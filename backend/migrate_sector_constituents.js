async function main() {
  // Table must be created in Supabase SQL editor first (see CREATE_TABLE above)
  // Try inserting — if table doesn't exist, this will fail with a helpful message
  console.log(`Seeding ${SEED.length} rows...`);
  const rows = SEED.map(([sector_key, symbol, name, isin, sort_order]) => ({
    sector_key, symbol, name, isin, sort_order
  }));

  // Delete existing data first (idempotent)
  const { error: delErr } = await supabase.from('sector_constituents').delete().neq('id', 0);
  if (delErr) {
    console.error('Delete failed (table may not exist yet):', delErr.message);
    console.log('\nPlease create the table first in Supabase SQL editor:');
    console.log(CREATE_TABLE);
    process.exit(1);
  }

  // Insert in batches of 100
  for (let i = 0; i < rows.length; i += 100) {
    const batch = rows.slice(i, i + 100);
    const { error } = await supabase.from('sector_constituents').insert(batch);
    if (error) {
      console.error(`Batch ${i}-${i+100} failed:`, error.message);
      process.exit(1);
    }
    console.log(`Inserted rows ${i+1}–${Math.min(i+100, rows.length)}`);
  }

  console.log('Done! sector_constituents seeded successfully.');
}

